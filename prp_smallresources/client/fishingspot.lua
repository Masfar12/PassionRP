-- Returns table
function shuffleSpots(spots)
    local out  = {}

    local rand = math.random

    for i = 1, #spots do
        local offset       = i - 1
        local val          = spots[i]
        local randomIndex  = offset * rand()
        local flooredIndex = math.floor(randomIndex)

        if flooredIndex == offset then
            out[#out + 1] = val
        else
            out[#out + 1]         = out[flooredIndex + 1]
            out[flooredIndex + 1] = val
        end
    end

    return out
end

