RegisterNUICallback('createNewCharacter', function(data)
    local cData = data
    DoScreenFadeOut(150)
    if cData.gender == "man" then
        cData.gender = 0
    elseif cData.gender == "women" then
        cData.gender = 1
    end

    TriggerServerEvent('prp-multicharacter:server:createCharacter', cData)
    TriggerServerEvent('prp-multicharacter:server:GiveStarterItems')
    Citizen.Wait(500)
end)

